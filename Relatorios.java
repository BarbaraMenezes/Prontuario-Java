    private void buPesquisarNomeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buPesquisarNomeMouseClicked
        // TODO add your handling code here:
        String arquivo = "MyReports/RelatorioNome.jasper";
        HashMap parametro = new HashMap<>();
        String nome = "%" + tfNome.getText() + "%";
        parametro.put("nome", nome);

        JasperPrint jp;

        try {
            jp = JasperFillManager.fillReport(arquivo, parametro, con);
            JasperViewer.viewReport(jp, false);
        } catch (JRException ex) {
            Logger.getLogger(Relatorios.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_buPesquisarNomeMouseClicked

