    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String arquivo = "MyReports/RelatorioCRM.jasper";
        HashMap parametro = new HashMap<>();
        Integer crm1 = Integer.parseInt(tfCRM1.getText());
        Integer crm2 = Integer.parseInt(tfCRM2.getText());
        parametro.put("crm1", crm1);
        parametro.put("crm2", crm2);

        JasperPrint jp;

        try {
            jp = JasperFillManager.fillReport(arquivo, parametro, con);
            JasperViewer.viewReport(jp, false);
        } catch (JRException ex) {
            Logger.getLogger(Relatorios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

